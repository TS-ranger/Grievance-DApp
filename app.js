window.onload = async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });

        const accounts = await web3.eth.getAccounts();
        account = accounts[0];

        contract = new web3.eth.Contract(abi, contractAddress);

        loadGrievances();
    }
};

async function submitGrievance() {
    const description = document.getElementById("description").value;
    const category = document.getElementById("category").value;

    await contract.methods
        .submitGrievance(description, category)
        .send({ from: account });

    alert("Submitted!");
    loadGrievances();
}

async function loadGrievances() {
    const count = await contract.methods.grievanceCount().call();

    const container = document.getElementById("grievanceList");
    container.innerHTML = "";

    for (let i = 1; i <= count; i++) {
        const g = await contract.methods.getGrievance(i).call();

        container.innerHTML += `
            <div class="card">
                <p><b>ID:</b> ${g[0]}</p>
                <p><b>Description:</b> ${g[2]}</p>
                <p><b>Category:</b> ${g[3]}</p>
                <p class="status ${g[4].replace(" ", "")}">
                    ${g[4]}
                </p>

                <button onclick="updateStatus(${g[0]}, 'InReview')">In Review</button>
                <button onclick="updateStatus(${g[0]}, 'Resolved')">Resolve</button>
            </div>
        `;
    }
}

async function updateStatus(id, status) {
    await contract.methods
        .updateStatus(id, status)
        .send({ from: account });

    loadGrievances();
}